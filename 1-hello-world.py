# print function - one param
print("Hello, World")  # output: Hello, World

# print function - two params
print("Hello", "World")  # output: Hello World

# print function - params: two strings and a separator string
print("Hello", "World", sep=", ")  # output: Hello, World

# print function - params: two strings, separator string, and end string
print("Hello", "World", sep=", ", end="!")  # output: Hello, World!
